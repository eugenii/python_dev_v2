# Обоснование алгоритма:
# Для решения задачи используется жадный алгоритм совместно с методом двух указателей.
# На вход функции solve() передается список весов роботов и максимальная грузоподъемность одной платформы.
# Сортировку проводим непосредственно в функции solve().
# Затем мы сопоставляем самого тяжёлого робота (указатель right) с самым лёгким (указатель left).
# Если их суммарный вес не превышает mass_limit, они могут поехать на одной платформе (мы сдвигаем оба указателя).
# Если они вместе не помещаются, то самый тяжёлый робот занимает платформу в одиночку (сдвигается только правый указатель),
# так как ни с каким другим роботом он уже точно не поместится. Процесс повторяется, пока все роботы не будут распределены.
# Жадный выбор на каждом шаге гарантирует минимальное итоговое количество платформ.
#
# Временная сложность (Time Complexity):
# Сортировка массива занимает O(N * log N), где N — количество роботов (алгоритм Timsort в Python).
# Обход массива двумя указателями занимает линейное время O(N), так как на каждом шаге цикла мы обрабатываем как минимум
# одного робота. Итоговая временная сложность: O(N * log N) + O(N) = O(N * log N).
# Она определяется самой ресурсоёмкой операцией — сортировкой.
#
# Пространственная сложность (Space Complexity):
# В оптимизированном варианте (in-place) мы сортируем исходный массив методом .sort().
# В Python встроенная сортировка Timsort требует до O(N) памяти в худшем случае для хранения временных данных.
# Никаких дополнительных структур данных (вроде deque) мы не создаём.
# Итоговая пространственная сложность: O(N).

import sys


def solve(probs: list[int], mass_limit: int) -> int:
    """Вычисляет минимальное количество платформ для перевозки роботов.

    Args:
        probs: Список целых чисел, содержащий веса роботов.
        mass_limit: Максимальная грузоподъёмность одной платформы.

    Returns:
        Минимальное количество транспортных платформ.
    """
    probs = sorted(probs)
    lightest_idx: int = 0
    heaviest_idx: int = len(probs) - 1
    platforms: int = 0
    
    while lightest_idx <= heaviest_idx:
        if probs[lightest_idx] + probs[heaviest_idx] <= mass_limit:
            lightest_idx += 1
        heaviest_idx -= 1
        platforms += 1
        
    return platforms


if __name__ == '__main__':
    data: list[int] = [int(i) for i in sys.stdin.readline().split()]
    print(solve(data, int(input())))
